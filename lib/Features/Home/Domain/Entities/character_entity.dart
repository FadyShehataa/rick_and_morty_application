class CharacterEntity {
  final String? image;
  final String? name;
  final String? status;
  final String? species;
  final String? type;
  final String? gender;

  CharacterEntity({
    required this.image,
    required this.name,
    required this.status,
    required this.species,
    required this.type,
    required this.gender,
  });
}
